package com.rockchip.devicetest.model;

public class VersionInfo {

	private String verName;
	private String verValue;
	
	/**
	 * @return the verName
	 */
	public String getVerName() {
		return verName;
	}
	/**
	 * @param verName the verName to set
	 */
	public void setVerName(String verName) {
		this.verName = verName;
	}
	/**
	 * @return the verValue
	 */
	public String getVerValue() {
		return verValue;
	}
	/**
	 * @param verValue the verValue to set
	 */
	public void setVerValue(String verValue) {
		this.verValue = verValue;
	}

}
